<?php
namespace Ajgon\LintPackBundle\DependencyInjection;

use Symfony\Component\Config\Definition\Builder\TreeBuilder;
use Symfony\Component\Config\Definition\ConfigurationInterface;

class Configuration implements ConfigurationInterface
{
    /**
     * {@inheritDoc}
     * @SuppressWarnings(PHPMD.ExcessiveMethodLength)
     */
    public function getConfigTreeBuilder()
    {
        $treeBuilder = new TreeBuilder();
        $rootNode = $treeBuilder->root('lint_pack');

        $rootNode
            ->children()
                ->arrayNode('jshint')
                    ->canBeEnabled()
                    ->children()
                        ->scalarNode('bin')
                            ->defaultValue('jshint')
                            ->cannotBeEmpty()
                        ->end()
                        ->scalarNode('jshintrc')
                        ->end()
                        ->scalarNode('jshintignore')
                        ->end()
                        ->arrayNode('extensions')
                            ->defaultValue(array('js'))
                            ->prototype('scalar')
                            ->end()
                        ->end()
                        ->arrayNode('ignores')
                            ->defaultValue(array())
                            ->prototype('scalar')
                            ->end()
                        ->end()
                        ->arrayNode('locations')
                            ->defaultValue(array('%kernel.root_dir%', '%kernel.root_dir%/../src'))
                            ->requiresAtLeastOneElement()
                            ->prototype('scalar')
                            ->end()
                        ->end()
                    ->end()
                ->end()
                ->arrayNode('csslint')
                    ->canBeEnabled()
                    ->children()
                        ->scalarNode('bin')
                            ->defaultValue('csslint')
                            ->cannotBeEmpty()
                        ->end()
                        ->arrayNode('disable_rules')
                            ->defaultValue(array())
                            ->prototype('scalar')
                            ->end()
                        ->end()
                        ->arrayNode('ignores')
                            ->defaultValue(array())
                            ->prototype('scalar')
                            ->end()
                        ->end()
                        ->arrayNode('locations')
                            ->defaultValue(array('%kernel.root_dir%', '%kernel.root_dir%/../src'))
                            ->requiresAtLeastOneElement()
                            ->prototype('scalar')
                            ->end()
                        ->end()
                    ->end()
                ->end()
                ->arrayNode('phpcs')
                    ->canBeEnabled()
                    ->children()
                        ->scalarNode('bin')
                            ->defaultValue('phpcs')
                            ->cannotBeEmpty()
                        ->end()
                        ->booleanNode('warnings')
                            ->defaultFalse()
                        ->end()
                        ->booleanNode('recursion')
                            ->defaultTrue()
                        ->end()
                        ->scalarNode('standard')
                            ->defaultValue('PSR2')
                        ->end()
                        ->arrayNode('extensions')
                            ->defaultValue(array('php'))
                            ->prototype('scalar')
                            ->end()
                        ->end()
                        ->arrayNode('ignores')
                            ->defaultValue(array())
                            ->prototype('scalar')
                            ->end()
                        ->end()
                        ->arrayNode('locations')
                            ->defaultValue(array('%kernel.root_dir%/../src'))
                            ->requiresAtLeastOneElement()
                            ->prototype('scalar')
                            ->end()
                        ->end()
                    ->end()
                ->end()
                ->arrayNode('phpmd')
                    ->canBeEnabled()
                    ->children()
                        ->scalarNode('bin')
                            ->defaultValue('phpmd')
                            ->cannotBeEmpty()
                        ->end()
                        ->arrayNode('rulesets')
                            ->defaultValue(array('codesize', 'controversial', 'design', 'naming', 'unusedcode'))
                            ->requiresAtLeastOneElement()
                            ->prototype('scalar')
                            ->end()
                        ->end()
                        ->arrayNode('extensions')
                            ->defaultValue(array('php'))
                            ->prototype('scalar')
                            ->end()
                        ->end()
                        ->arrayNode('ignores')
                            ->defaultValue(array())
                            ->prototype('scalar')
                            ->end()
                        ->end()
                        ->arrayNode('locations')
                            ->defaultValue(array('%kernel.root_dir%/../src'))
                            ->requiresAtLeastOneElement()
                            ->prototype('scalar')
                            ->end()
                        ->end()
                    ->end()
                ->end()
                ->arrayNode('phpcpd')
                    ->canBeEnabled()
                    ->children()
                        ->scalarNode('bin')
                            ->defaultValue('phpcpd')
                            ->cannotBeEmpty()
                        ->end()
                        ->scalarNode('min_lines')
                            ->defaultValue('5')
                        ->end()
                        ->scalarNode('min_tokens')
                            ->defaultValue('70')
                        ->end()
                        ->arrayNode('extensions')
                            ->defaultValue(array('php'))
                            ->prototype('scalar')
                            ->end()
                        ->end()
                        ->arrayNode('ignores')
                            ->defaultValue(array())
                            ->prototype('scalar')
                            ->end()
                        ->end()
                        ->arrayNode('locations')
                            ->defaultValue(array('%kernel.root_dir%/../src'))
                            ->requiresAtLeastOneElement()
                            ->prototype('scalar')
                            ->end()
                        ->end()
                    ->end()
                ->end()
                ->arrayNode('twig')
                    ->canBeEnabled()
                    ->children()
                        ->arrayNode('ignores')
                            ->defaultValue(array())
                            ->prototype('scalar')
                            ->end()
                        ->end()
                        ->arrayNode('locations')
                            ->defaultValue(array('%kernel.root_dir%', '%kernel.root_dir%/../src'))
                            ->requiresAtLeastOneElement()
                            ->prototype('scalar')
                            ->end()
                        ->end()
                    ->end()
                ->end()
            ->end()
        ->end();

        return $treeBuilder;
    }
}
